namespace Unity.Media.Osc
{
    /// <summary>
    /// An enum defining the type of an address.
    /// </summary>
    public enum AddressType
    {
        /// <summary>
        /// The address is not a valid OSC Address Pattern.
        /// </summary>
        Invalid,

        /// <summary>
        /// The address is an OSC Address.
        /// </summary>
        Address,

        /// <summary>
        /// The address is an OSC Address Pattern.
        /// </summary>
        Pattern,
    }
}

